function useCompundoperators() {
    let x = 5;
    console.log("Initial value of X is", x);
    x += 2;
    console.log("After addition value of X is", x);
    x -= 2;
    console.log("After substraction value of X is", x);
    x *= 2;
    console.log("After multiply value of X is", x);
    x /= 2;
    console.log("After division value of X is", x);

}
useCompundoperators()